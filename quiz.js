// عرض السؤال + الخيارات
function displayQuestion() {
  if (currentQuestion >= questions[currentLevel].length) {
    showResult();
    return;
  }

  let q = questions[currentLevel][currentQuestion];
  const questionEl = document.getElementById("question");
  questionEl.textContent = q.question;
  questionEl.style.background = "linear-gradient(135deg, #f70c33ff, #ffc3a0)";
  questionEl.style.color = "#000";
  questionEl.style.padding = "20px";
  questionEl.style.borderRadius = "15px";
  questionEl.style.fontSize = "25px";
  questionEl.style.marginBottom = "20px";

  const optionsContainer = document.getElementById("options-container");
  optionsContainer.innerHTML = "";
  q.options.forEach((opt, idx) => {
    let btn = document.createElement("button");
    btn.textContent = opt;
    btn.className = "option-btn";
    btn.onclick = () => checkAnswer(idx);
    btn.style.margin = "5px";
    btn.style.padding = "15px 25px";
    btn.style.fontSize = "20px";
    btn.style.borderRadius = "10px";
    btn.style.border = "none";
    btn.style.cursor = "pointer";
    btn.style.transition = "all 0.3s ease";
    btn.style.background = "linear-gradient(135deg,rgba(4, 129, 253, 1), #fc0404ff, rgba(4, 129, 253, 1))";
    btn.style.color = "#ffffff";
    btn.onmouseover = () => {
      btn.style.transform = "scale(1.1)";
      btn.style.background = "rgba(3, 250, 155, 0.13)";
    };
    btn.onmouseleave = () => {
      btn.style.transform = "scale(1)";
      btn.style.background = "linear-gradient(135deg,rgba(4, 129, 253, 1), #fc2104ff, rgba(4, 129, 253, 1))";
    };
    optionsContainer.appendChild(btn);
  });
}

// تحقق الإجابة
function checkAnswer(selected) {
  if (selected === questions[currentLevel][currentQuestion].answer) score++;
  currentQuestion++;
  displayQuestion();
}

// المؤقت
function startTimer() {
  const timerEl = document.getElementById("timer");
  timerInterval = setInterval(() => {
    let minutes = Math.floor(totalTime / 60);
    let seconds = totalTime % 60;
    timerEl.textContent = `${minutes.toString().padStart(2, "0")}:${seconds
      .toString()
      .padStart(2, "0")}`;
    totalTime--;
    if (totalTime < 0) {
      clearInterval(timerInterval);
      showResult();
    }
  }, 1000);
}

// عرض النتيجة
function showResult() {
  clearInterval(timerInterval);
  window.location.href = `result.html?score=${score}&total=${questions[currentLevel].length}`;
}

// بدء الكويز حسب المستوى
function startQuiz(level) {
  currentLevel = level;
  currentQuestion = 0;
  score = 0;
  totalTime =
    level === "easy"
      ? 30 * 60
      : level === "medium"
      ? 25 * 60
      : level === "hard"
      ? 35 * 60
      : 50 * 60; // جنوني
  displayQuestion();
  addExitButton(); // إضافة زر الخروج
  startTimer();
}

